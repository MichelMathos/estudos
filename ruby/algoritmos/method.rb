def talk (first_name, last_name) #definindo uma função chamada 'talk', que recebe dois parâmetros: 'first_name' e 'last_name'
    puts "Olá, #{first_name} #{last_name}! Como você está?" #esta linha contém o código que será executado quando a função 'talk' for chamada
end

primeiro = "Michel" 
segundo = "Matos"

talk(primeiro, segundo) #aqui estamos chamando a função 'talk' que definimos anteriormente. Estamos passando as variaveis 'primeiro' e 'segundo' como
                        #argumento para a função, ou seja, essas variáveis substituem o modelo definido anteriormente, aplicando seus valores '"Michel"' e '"Matos'".