# atribuição simples com =
# sobrescreve o valor

saldo = 500
print (saldo)

saldo = 200
print (saldo)

# soma um valor a sí mesmo
saldo += 10
print(saldo)

# subtrai um valor de si
saldo -= 5
print (saldo)

# se divide por um valor. resultando em float
saldo /= 2
print(saldo)

# se divide por um valor, resultando num inteiro
# (desde que o valor da variável seja um numero inteiro)
saldo //=2
print(saldo)

# se multiplica por um valor
saldo *= 10
print(saldo)

# faz seu módulo 
saldo %=4
print(saldo)

# faz sua exponenciação
saldo **=2
print(saldo)